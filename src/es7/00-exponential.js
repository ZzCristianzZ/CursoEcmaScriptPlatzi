/*En ECMAScript 6, se podía programar un exponencial mediante la función Math.pow(). Esta función toma dos parámetros: el primer parámetro es un número base y el segundo parámetro es el exponente. Por ejemplo, Math.pow(2, 3) devolverá 8, que es 2 elevado a 3. */